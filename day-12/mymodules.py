# Creating functions to make a module
def get_fullname(first_name, last_name):
    space = ' '
    full_name = first_name + space + last_name
    return full_name


def add_two_numbers(num1, num2):
    total = num1 + num2
    return total

def area_of_circle(radius):
    pi = 3.14
    area = pi * radius ** 2
    return area